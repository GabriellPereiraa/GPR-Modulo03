                                <h2 class="Titulo" >1 - Princípios e Dimensões da Nova Gestão Pública</h2>
                                <br>

                                <p class="Texto">A evolução da Administração Pública, apresentada na aula 2, mostra o desenvolvimento dos paradigmas predominantes da administração pública ao longo dos anos. Estes, porém, não são estanques no tempo, fazendo com que características dos vários paradigmas coexistam, sobretudo se considerarmos os diversos graus de maturidade da administração pública no mundo, seja nos governos nacionais, seja nos subnacionais.</p>

                                <p class="Texto">Ademais, ainda que um novo paradigma surja com a intenção de resolver as limitações presentes no paradigma anterior, ele sempre carrega em si influências dos modelos anteriores, num processo evolutivo que contribui para o aprimoramento da teoria e prática da gestão pública. Nenhum novo paradigma se instala imediatamente, ainda que seja impulsionado por modelos de reforma administrativa que estabeleçam novos processos, ferramentas e instrumentos de gestão, além de transformações na estrutura organizacional. A mudança de cultura, do pensar e do fazer diferente, leva tempo e ganha novos contornos e adaptações a diferentes culturas.</p>

                                <p class="Texto">Dessa forma, o que se vê na prática da gestão pública no mundo é um mosaico de experiências que mesclam elementos de vários paradigmas – do patrimonialismo à governança pública –, trazendo singularidade à evolução da gestão pública em cada localidade.</p>

                                <p class="Texto">Nesse momento, considera-se relevante revisitar os Princípios da Nova Gestão Pública, de alguma forma mencionados no módulo 2, e a descrição das principais dimensões de análise do modelo GpR, que servirão de orientação conceitual para o desenvolvimento do modelo no Ceará.</p>

                                <p class="Texto">O paradigma da Nova Gestão Pública surgiu e se consolidou progressivamente sob vários formatos e em numerosos países, de forma desigual e com diferentes graus de sucesso. Em termos geográficos, embora tenha sido gerado e adotado inicialmente pelos países de língua inglesa - especialmente Reino Unido, Austrália, Nova Zelândia, Estados Unidos e Canadá -, ele se disseminou praticamente por todos os continentes e por países muito diversos, convivendo e se adaptando a culturas, desafios e problemas bastante distintos. Portanto, a Nova Gestão Pública foi implantada sob distintos modelos, perspectivas, correntes e ondas, dificultando uma síntese única e inequívoca de todas as experiências vivenciadas.</p>

                                <p class="Texto">No entanto, mesmo considerando variações, evoluções e dissonâncias nas suas aplicações desde os anos 1980, seguem alguns princípios que costumam fundamentar os diversos modelos elaborados a partir da Nova Gestão Pública.</p>

